        String session=reg_no.split("-")[0];
        if (!password.equals(c_password)){
            JOptionPane.showMessageDialog(null, "Check password again");
            return;
        }
        String query="INSERT INTO student VALUES('"+name+"','"+reg_no+"','"+department+"','"+session+"','"+CNIC+"','"+email+"','"+password+"')";
        InsertUpdateDelete.addData(query, "You are registered");
