       String c_password=jPasswordField2.getText();
      
       if (name.equals("") || reg_no.equals("") || CNIC.equals("") || email.equals("") || password.equals("") || c_password.equals("") ){
           JOptionPane.showMessageDialog(null, "All fields must be fill" );
           return;
    }
